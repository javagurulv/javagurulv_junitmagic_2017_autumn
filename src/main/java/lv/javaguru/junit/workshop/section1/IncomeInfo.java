package lv.javaguru.junit.workshop.section1;

public class IncomeInfo {

    private int year;
    private double income;

    public IncomeInfo(int year, double income) {
        this.year = year;
        this.income = income;
    }

    public int getYear() {
        return year;
    }

    public double getIncome() {
        return income;
    }
}
